<h1>UKPL Competition Rules &amp; Guidelines</h1>       

<h2>1.	General information</h2>

<p class="bold">A. Regional rules</p>
<p>These rules apply to regional competitions only and not the UK Grand Final which may have its own special rules.</p>

<p class="bold">B.	Number of meetings</p>
<p>A minimum of four and maximum of six league meetings will be held in each region per year at least three different venues to create a regional league competition. No venue should be used more than twice in the season.</p>

<p class="bold">C.	Competing in more than one region</p>
<p>Players can register for one regional league only. Meetings will be open to all players but only points scored in a players region will count towards their league total.</p>

<p class="bold">D.	Fees</p>
<p>A fee of £2.00 must be collected by the regional coordinator or deputy from all adult players before any play begins – children play for free. This will contribute to a central prize fund. An additional voluntary contribution is welcomed and is given to the host to help towards providing refreshments. Hosts and their immediate family members will be exempt from the entry fee for the meet which they host. Hosts (but not their family members) are also exempt from paying the entry fee for every meet they attend in their region for that season.</p>

<p class="bold">E.	Minimum number of competitors</p>
<p>For a meeting to count towards the regional league at least five competitors from that region must take part.</p>

<p class="bold">F.	Finals</p>
<p>League Finals are split into A and B divisions. At the end of the league season the top players from each region will be able to compete in a UK individual Grand Final competition.</p>

<p class="bold">G.	Finals qualification</p>
<p>The number of players who can qualify for the finals from each region is decided by the average attendance for each region.  The number of finalists for each region will be weighted between each region’s average attendance with a minimum of three qualifiers per region and a maximum of five (or six in exceptional cases). The A Finals will comprise the following players:</p>
<p>South West - top four finishers<br>
Midlands - top four finishers<br>
London &amp; South East - top six finishers<br>
Northern - top five finishers<br>
Scottish - top three finishers<br>
Irish - top three finishers</p>
<p>The B Finals will comprise the next four finishers from each region.</p>
<p>This gives a total of 26 players with no reserves, substitutions or promotions for people who don't attend. The format is league-style qualifying over five games. The top two progress to the final with a bye while 3rd-6th play off for the two remaining places in the final.</p>

<p>The aim is to ensure strong regional representation with a final of the best players in the land. It is inclusive rather than exclusive and encourages participation in the smaller regional leagues.</p>

<p class="bold">H.	WPPR points</p>
<p>World Pinball Player Ranking points are awarded to all competitors based on how well they perform at each meet.  Current world and UK rankings can be viewed at <a href="http://www.pinballrankings.com/country.php" class="link" target="_blank">www.pinballrankings.com</a>.</p>

</div>

<div class="panel">

<h2>2.	Machines</h2>

<p class="bold">A.	Machine selection</p>
<p>Games played will be decided in advance of each meeting by the organizers. </p>

<p class="bold">B.	Minimum number of machines</p>
<p>Meetings will have at least four games available for the competition (the more the better) unless agreed with the regional coordinator and publicized in advance.</p> 

<p class="bold">C.	Machine owners</p>
<p>Competition machine owners or those involved in organizing competitions may enter. Any advantage gained will be insignificant when spread over the other venues/meetings and is viewed as a benefit of hosting.</p>

<p class="bold">D.	Machine settings</p>
<p>Games should be set to tournament settings and to exclude extra balls and unfair random awards where possible. (Note: also see rules section 5). Four balls will be played by each player per game where possible. Note: this may not be possible on older games.</p> 

<p class="bold">E.	Random jackpots</p>
<p>To allow the inclusion of games with random jackpots (mainly late '80s games), the regional coordinater may pre-agree a games jackpot value e.g if the value varies between 1m and 10m, they may decide choose to set the jackpot value at 5m.</p>

<p>The co-ordinator will advise players of the pre-agreed jackpot value before play begins. Note: This can only be done if it is clearly possible to record the jackpot awarded during the game and if it can be verified by independent adjudication e.g. by other players, an adjudicator or the coordinater.</p>

<p>The jackpot(s) value awarded can then be recorded and the player’s actual score adjusted to give the final score with the pre-agreed fixed jackpot value.</p>

<p class="bold">F.	Multiplayer games</p>
<p>At least one competition game must be designated as a multiplayer game, on which groups of between two and six players will play together. More games may be played as multiplayer games at the discretion of the regional co-ordinator. Single player games should be played first so as not to exclude players who may arrive late.</p>

</div>

<div class="panel">

<h2>3.	Scoring (includes UK Pinball League Tables)</h2>

<p class="bold">A.	General</p>
<p>A player’s individual game scores will be recorded and used to determine finishing positions for each meeting. These meeting positions (see 3b &amp; 3d) are used to calculate regional league points and are posted on the UK Pinball League web site league tables.</p>

<p class="bold">B.	Machine points</p>
<p>The highest number of points awarded on each game at a league meeting is equal to the number of competitors e.g. if there are 20 players the highest scorer on each game gets 20 points and the lowest 1 point. The finishing order at each meeting is decided by adding together individual player’s points from each competition game played, for a meeting total.</p>
<p>One  bonus machine point is awarded to any player whose score on any machine is more than double that of the second placed player on the same machine.</p>
<p class="bold">C.	League points for players from other regions</p>
<p>League (and WPPR) points can only be awarded to players registered in that region. Players can compete in any regional meeting for fun but if playing in a region other than their own; their finishing positions will not feature when league points are awarded.</p> 

<p class="bold">D.	League points</p>
<p>League points are awarded to the twenty highest placed players from that region e.g. if there are twenty players the player with the most machine points is awarded twenty league points and the lowest one league point. Note: If there are less than twenty players, twenty league points are still awarded to the best player and the score will reduce by one league point per finishing place until all players have their allocation.</p>

<p class="bold">E.	Overall points</p>
<p>A player’s top four meeting scores throughout the season will count towards their final league position. The other scores will be discarded so the maximum league score possible for a player is 80 (4x20) points.</p>

<p class="bold">F.	Ties</p>
<p>In the event of a tie at a meeting points will be shared. In the event of a tie at the end of the season, for a finals qualifying place, the player’s fifth and then if required sixth next best scores will be taken into consideration until the final positions are decided. If after this the result is still tied then a sudden death playoff (one game on one machine) should be organized to determine the final positions.</p>

<p class="bold">G.	League tables</p>
<p>The UK Pinball League web site league tables will display the latest regional league point standings and highlight the grand final qualification places.</p> 

</div>

<div class="panel">

<h2>4.	Regional coordinators</h2>

<p class="bold">A.	Meetings</p>
<p>Regional coordinators are responsible for organizing and coordinating the regional meetings and liaising with hosts.</p>

<p class="bold">B.	Stewards</p>
<p>Regional coordinators will act as central contacts and adjudicators for their regions. Regional coordinators or meeting hosts must ensure that a steward or stewards are present at the meeting to monitor player conduct and ensure fair play.</p>

<p class="bold">C.	Reporting results</p>
<p>Regional coordinators or deputies must report league point awards to the UK Pinball League web site organizer within one week of the end of a regional meeting and should also post the meeting results on the UK pinball group.
Note: also see sections 3 scoring and 5 rules.</p>

</div>

<div class="panel">

<h2>5.	Rules</h2>

<p class="bold">A. 	Practice</p>
<p>Players will be able to practice for at least one hour on the competition games before the competition begins.</p>

<p class="bold">B. 	General play</p>
<p>The interpretation of these rules along with any resolution of matters not covered by these rules is at the sole discretion of the competition organizers whose decisions in the matters are final.  They shall, however, attempt to behave in what they believe to be a fair and equitable way at all times.</p> 

<p class="bold">C.	Game start</p>
<p>Entrants must wait until the game becomes available and then step up to the game when directed to do so by a competition steward. Entrants may not start or restart any of the games unless instructed to do so by the steward.</p> 

<p class="bold">D.	Game completion</p>
<p>When the entrants have completed their game they must attract the attention of a steward who will record their nominated game score and initial the score card to verify that all is correct. Players may record their own score but must have the result witnessed by a steward after each game.  When this has been done the entrant must initial the details to verify that they are correct.  Once this has been completed, no discussions about the correct recording of these details will be entertained.  The details will, in due course, be passed to the competition organizer, regional coordinator or steward as instructed on the day.</p> 

</div>

<div class="panel">

<h2>6.	Machine Faults</h2>

<p class="bold">A.	Permanent faults</p>
<p>If the chosen machine becomes inoperable or the entrant's score has been unduly affected during the course of the game, the machine will be taken out of the competition until the fault can be repaired and the entrants will be offered the chance to replay the game.</p>

<p>The selection of games is not fixed and may change during the competition. If a game has to be removed during the course of the competition, a substitute game will be provided where possible and any games already played on the faulty machines will need to be replayed on the new game.</p>

<p class="bold">B. Temporary faults with no loss of ball</p>
<p>If a mechanical or software fault develops during the course of the game, the entrant must attract the attention of the steward who will attempt to resolve the situation.  If the ball becomes stuck, the entrant may attempt to free it at the risk of tilting the game. Otherwise they should attract the attention of the steward who will attempt to free it and place it in a dead area of the game such as the shooter lane or in a ball trap so the entrant may continue play.</p>

<p class="bold">C. Temporary faults with loss of ball</p>
<p>If a player loses a ball through a random machine fault which is not persistent and has not affected other players and through no fault of their own, the player affected has two options as below.  The player must notify the regional co-ordinator and explain the fault which must be corroborated by another player.  It is up to the regional co-ordinator to decide if the event falls under this rule.  Their decision will be final but they may choose to consult with other players to deem what is the fairest solution.</p>

<p>1. Completing their game and then playing one ball of a new game.  The scores of the two games will then be added together to form the player’s score for that machine. Or,</p>

<p>2. Discarding the current game and replaying the game from scratch.</p>

<p>The player choose which option they wish to take and notify the regional co-ordinator accordingly.  Once decided they cannot change ie the player cannot continue their current game and then request to discard this game and start a whole new game – they must decide at the time of the fault.  On option 1, the one ball they play will be from a new game and no attempt should be made to manually award features that may have been achieved in the faulty game.  If the game was part of a multiplayer game and the other players’ games were unaffected, these players should complete their games as normal irrespective of the choice the affected player makes.</p>

<p class="bold">D. Non-working features</p>
<p>If a game has a known fault such as a non working feature, it may be used for the competition provided that it is agreed by the regional coordinator and provided that all players are made aware of the problem.</p>

</div>

<div class="panel">

<h2>7. Player Conduct</h2>

<p class="bold">A. Behaviour</p>
<p>Entrants should play their nominated game to the best of their ability with good conduct and without cheating, distracting other players or unfairly influencing the score. The organizers shall decide what constitutes good conduct and cheating. Any entrant deemed to be in breach of these rules may be warned or disqualified and their score adjusted or discarded.</p>

<p>Unruly and offensive behaviour will not be tolerated.  This includes but is not limited to:</p>

<p>1. Machine abuse such as using excessive force to move the machine causing it to knock into an adjacent machine or come off a leg support (if applicable), banging on the glass or lockdown bar or generally shaking or banging the machine in frustration after the ball has drained.</p>

<p>2. Swearing and other loud AND persistent shouting during play.</p>

<p>It is up to the regional co-ordinator's discretion to apply any penalty on the day.</p>

<p>As a guide for less serious incidents, a warning will be given but if there is a repeat offence on the day or next meet, a penalty will be applied.  For more serious offences, a penalty will be applied on the day but offences deemed serious enough will be reported to the league to consider if any further penalty is deemed necessary.</p>

<p>When deciding the appropriate penalty, the regional co-ordinator will seek feedback from other players if they didn't witness the incident themselves and will consider the intent even if no damage was caused and the damage (if applicable).  The offender will be expected to pay for any damage caused to the host's machines caused by abuse.</p>

<p>Penalties will be one of the following:<br>
- award zero score on the machine the offence was committed on, or to zero another score if the offence occurred whilst the player involved was not competing at the time.<br>
- disqualify the player from the meet, ie zero all scores on all machines at the meet<br>
- a ban for the next meet<br>
- a ban for the season</p>

<p>For any player who has already received a penalty during the season, there will be no further warnings and for the next offence, the next level of penalty above will be imposed as a minimum.</p>

<p class="bold">B.	Coaching</p>
<p>Any coaching or advice to players, by spectators or other players is not allowed during a competitive game but the rules of the game may be discussed before starting play.</p>

<p class="bold">C.	Extra balls</p>
<p>No extra balls shall be played in any game. If a player earns an extra ball they may plunge the ball and go for a plunger skill shot, but may not touch the machine in any way after the ball is plunged. The player may touch the flippers before plunging the ball in order to set up for a skill shot, etc.</p> 

<p>If a player mistakenly plays an extra ball (meaning they hit either or both flippers, or bump the machine at any time during the extra ball, other than as specified in H), they must stop playing as soon as the error is recognized and they may not play their next ball. If they are on their last ball then their final score is calculated as 80% of their total score.</p> 

<p>If a player is recognized to be playing an extra ball, is clearly told so, and does not stop playing the ball within 10 seconds, they receive zero points for the entire game instead of only losing the next ball.</p>

<p class="bold">D.	Tilts</p>
<p>Tilting the machine during a game will result in the loss of the entrant's current ball. Slam tilting will result in the game ending and the offending player automatically losing that game and being awarded a score of zero.</p>

<p>If this occurs in a multiplayer game, the other players have the choice of the option in rule 6c assuming that the players can agree on their score </p>

<p>Note on some older games tilt will cause loss of the entire game and not just the ball in play.</p>

<p>If in the opinion of the organizers the slam tilt is due to poor conduct by the entrant, the entrant may be disqualified and their score discarded.</p>

</div>

