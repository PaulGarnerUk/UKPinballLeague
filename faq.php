<h1>UKPL FAQs</h1>

<p class="bold firstline">How do I join the league?</p>
<p><a href="contacts.php" class="link">Contact</a> your regional co-ordinator who will let you know the dates and locations of meets in your region and add you to the update email.  You can also see these on the <a href="schedule.php" class="link">Schedules</a> page.</p>


<p class="bold">Where are the meets held?</p>
<p>We have six active regions which form the UK Pinball League – South East, South West, Midlands, Northern, Scottish &amp; Irish. Meets are held at league members’ houses.</p>


<p class="bold">How often are meets held?</p>
<p>We aim to hold six meets per region each season although this varies region to region depending on the availability of venues.</p>


<p class="bold">When are the meets held?</p>
<p>Please see the <a href="schedule.php" class="link">Schedules</a> page.  Meets tend to be on a Sunday starting around noon with a couple of hours to practice and socialize and the competition starting around 14:00, but this varies slightly by region.</p>


<p class="bold">When does the season run?</p>
<p>The season starts in January and continues until autumn.</p>


<p class="bold">How much does it cost?</p>
<p>There’s a charge of £2.00 for each meet you attend which goes towards the cost of prizes and running the league. Also there's often a voluntary contribution to the host which goes towards providing refreshments.</p> 


<p class="bold">Are refreshments provided?</p>
<p>Yes, there are free snacks and drink provided by the host at each meet.</p>


<p class="bold">I’m worried I’m not very good.</p>
<p>Don’t worry! There’s a wide range of players and abilities in each region so  you won’t feel as if you’re out of your league and everyone is very friendly.  There’s also a chance to play a wide variety of machines in good condition and make new friends.</p>


<p class="bold">Which machines are played in the league?</p>
<p>The machines vary depending on who’s hosting the meet.  They tend to mostly be the newer machines from the early 90s to present day but some meets include early and late 80s games and occasionally 60s and 70s games.</p>


<p class="bold">Is it only middle-aged blokes?</p>
<p>No, we welcome and encourage players of all ages.  We usually have several children and women playing at each meet and there is a prize for the best in each category (see below).</p>


<p class="bold">How many people do there tend to be at each  meet?</p>
<p>Again varies by meet and region but is anywhere between 10 and 40.</p>


<p class="bold">How long has the league been running?</p>
<p>The league was set up in 2006 and is now in its 12th season</p>


<p class="bold">Can I host a meet?</p>
<p>YES, we're always looking for new members to host meetings.You will need a minimum of four machines to host a meet and ideally six or more. Please <a href="contacts.php" class="link">contact</a> your regional co-ordinator to discuss further who will also be able to offer you support in hosting your meeting.</p>


<p class="bold">Which region do I join?</p>
<p>That depends on where you live but it’s your choice.  <a href="contacts.php" class="link">Contact</a> your regional co-ordinator to find where the meets are usually held in that region.</p>


<p class="bold">Can I join more than one region?</p>
<p>No, you can only compete in one region.  However, you are welcome to attend meets in other regions and compete for fun but your scores will not count towards the league.</p>


<p class="bold">Can I change the region I’m competing in?</p>
<p>Possibly.  You’ll need to <a href="contacts.php" class="link">contact</a> the overall co-ordinator who will discuss and determine whether this is possible. In general, you can change region at or before the start of the season but not mid season.</p>


<p class="bold">How does the competition work?</p>
<p>You play between three and eight machines depending on the venue.  Your score is recorded and verified and then ranked against all other players with the top score getting the same number of points as the number of players and then each player one less point so that last gets one. Eg for 20 players, 1st gets 20 machine points, 2nd 19, 3rd 18 etc and 20th 1.  The total number of machine points you got on all machines is then totaled and ranked against all other players.  League points are then awarded to the top players as follows:</p>
 

